# 4.Design a class `Calculator` with methods for addition, subtraction, multiplication, and division. Each
# method should take two numbers as parameters and return the result.
class Calculator:
    def add():
        a,b=10,5
        c=a+b
        print(c)
    def sub():
        a,b=10,5
        c=a-b
        print(c)
    def mult():
        a,b=10,5
        c=a*b
        print(c)
    def div():
        a,b=10,5
        c=a/b
        print(c)
n1=Calculator
n1.add()
n1.sub()
n1.mult()
n1.div()